/**
 * Test system endian.
 * @returns  Big(1) or litte(0) endian.
 */
int endian(void)
{
    unsigned char data[4] = { 0x11, 0x22, 0x33, 0x44 };

    return (0x11223344 == *((int *)data));
}

/**
 * Test system bit number.
 * @returns  Bits.
*/
int bitsize(void)
{
    return (sizeof(void *) * 8);
}

